%%输入:已知点（4个）的坐标(u,v)（4*1,4*1）以及在该帧中的相对位移d(dx/dy)（4*1）；图像的高h，宽w
function cornxy=corner_xy(u,v,d,h,w)
%1.首先先解四元一次方程组
%系数
% w=2*a;      
% h=2*b;
N1=(u-w).*(v-h)./((1-w).*(1-h));       %型函数（4*1）
N2=(u-1).*(v-h)./((w-1).*(1-h));
N3=(u-1).*(v-1)./((w-1).*(h-1));
N4=(u-w).*(v-1)./((1-w).*(h-1));
%组成方程组的系数矩阵
A=[N1,N2,N3,N4];            %4*4
%常量向量b
b=d;                      
% 解方程组
cornxy = A \ b;
%以上获得了四个角点的相对位移dx1234